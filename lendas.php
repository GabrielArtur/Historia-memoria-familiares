                    <p>Ana Rosa, nascida por volta de 1865 em Avaré (embora alguns digam São José dos Campos), tornou-se uma figura lendária em Botucatu após um crime passional brutal que marcou o século 19. Casada com Francisco de Carvalho Bastos, conhecido como Chicuta, ela sofria com os ciúmes e agressões do marido e decidiu fugir para Botucatu. Lá, foi acolhida por Fortunata Jesuína de Melo, dona de um cabaré, e passou a ser conhecida como uma mulher bonita e popular</p>
                    <p>Enfurecido com a fuga, Chicuta contratou dois homens — Costinha e Minigirdo — para matá-la. Ana Rosa foi enganada por Costinha, que fingiu ajudá-la, e acabou emboscada nas margens do ribeirão Lavapés, onde foi esquartejada em 1885, aos 20 anos. A lenda diz que, enquanto seus restos eram levados pelas ruas, o ar se enchia com cheiro de rosas.</p>
                    <p>Os assassinos e o mandante tiveram mortes trágicas: Costinha foi esmagado por uma árvore, Minigirdo morreu de varíola na prisão, e Chicuta teve a cabeça arrancada por seu próprio carro de boi.</p>
                    <p>No local do crime, populares ergueram um cruzeiro e depois uma capela, que se tornou ponto de devoção popular — muitos acreditavam que Ana Rosa fazia milagres. O bispo de Botucatu chegou a proibir o culto na década de 1920, mas o local permaneceu venerado. Hoje, a Capela da Santa Cruz de Ana Rosa é tombada como patrimônio histórico e voltou a receber missas, reinterpretada pela Igreja como símbolo de reflexão sobre a mulher e a fé cristã.</p>
